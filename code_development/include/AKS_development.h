#ifdef _M_IX86
#define umulrem(z, x, y, m)	\
	__asm	mov		eax, x	\
	__asm	mul		y	\
	__asm	div		m	\
	__asm	mov		z, edx
#define umuladdrem(z, x, y, a, m)	\
	__asm	mov		eax, x	\
	__asm	mul		y	\
	__asm	add		eax, a	\
	__asm	adc		edx, 0	\
	__asm	div		m	\
	__asm	mov		z, edx
#else
#ifdef _MSC_VER
	typedef unsigned __int64	Tu64;
#else
	typedef unsigned long long	Tu64;
#endif
#define umulrem(z, x, y, m)	\
	{	\
	z = (unsigned int)(x * (Tu64)y % m);	\
	}
#define umuladdrem(z, x, y, a, m)	\
	{	\
	z = (unsigned int)((x * (Tu64)y + a) % m);	\
	}
#endif
